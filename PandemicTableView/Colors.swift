import UIKit

extension UIColor {
    
    class func dialogBlue() -> UIColor {
        return UIColor(red: 0, green: 1, blue: 1, alpha: 1.0)
    }
    
    class func loadingBlue() -> UIColor {
        return UIColor(red: 0.267, green: 0.51, blue: 0.871, alpha: 1.0)

    }
    
    class func settingBlue() -> UIColor {
        return UIColor(red: 0.318, green: 0.902, blue: 0.976, alpha: 1.0)
    }
    
    class func statusGreen() -> UIColor {
        return UIColor(red: 0, green: 1, blue: 0.239, alpha: 1.0)
    }
}
